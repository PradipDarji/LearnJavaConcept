package ControlStatement;

public class DoWhileLoop {

	public static void main(String[] args) {

		int i = 0;
		do {
			i++;
			System.out.println(i);
		} while (i <= 10);
		
		System.out.println("****************************");
		int k = 0;
		do {
			System.out.println(k++);
		} while (k <= 10);

	}

}
